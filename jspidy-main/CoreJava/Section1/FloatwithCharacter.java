//Write a program to add a float value and a character value
class FloatwithChar
{
	public static void main(String[] args)
	{
		float  var1 = 10.5f;
		char var2 = '\n';
		System.out.println("------------------------------");
		System.out.println("var1 + var2 = "+ var1 + var2 );
		System.out.println("var1 + var2 = "+ (var1 + var2) );
		System.out.println("------------------------------");
	}
}