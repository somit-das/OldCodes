class ExampleOperator10
{
	public static void main(String[] args)
	{
		int a = 5 , b = 3;
		System.out.println( a & b );
		System.out.println( a | b );
		System.out.println( a ^ b );
		int num = 5;
		System.out.println(~num);
		
		int n = 20;
		System.out.println( n >> 2 );
		System.out.println( n << 2 );
	}
}
