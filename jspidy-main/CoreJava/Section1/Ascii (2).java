// wap to find the ASCII value of the characters @ $ m 7 Q
class Ascii
  {
    public static void main(String[] args)
    {
      char c1='@';
      char c2='$';
      char c3='m';
      char c4='7';
      char c5='Q';
    
     System.out.println("The ASCII VALUE @:"+(int)c1);
     System.out.println("The ASCII VALUE $:"+(int)c2);
     System.out.println("The ASCII VALUE m:"+(int)c3);
     System.out.println("The ASCII VALUE 7:"+(int)c4);
      System.out.println("The ASCII VALUE Q:"+(int)c5);
     }
  }

   