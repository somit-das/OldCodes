// Write a program to find the ASCII value of the characters  @, $ , m ,7,Q
class AsciiChar
{
	public static void main(String[] args)
	{
		int a = '@';
		int b = '$';
		int c = 'm';
		int d = '7';
		int e = 'Q';
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}