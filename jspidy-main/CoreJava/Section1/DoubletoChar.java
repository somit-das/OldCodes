//Write a Program to convert double value into a character.

class DoubletoChar
{
	public static void main(String[] args)
	{
		double interest = 56.534d;
		char ch_interest = (char) interest;
		System.out.println("----------------------------------------------------------");
		System.out.println("Character Converted Interest is " +  ch_interest);
		System.out.println("----------------------------------------------------------");
	
	}
}